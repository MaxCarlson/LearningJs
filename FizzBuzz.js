//Used for printing Fizz-Buzz's at multiples of 3 and 5 and their respective Fizz's and Buzz's for multiples of only  3 or 5

for(i=1; i<=100; i++ ){
    var fizz = i%3
    var buzz = i%5
    if(fizz + buzz === 0) {
        console.log("Fizz-Buzz");
    }
    else if(buzz === 0){
        console.log("Buzz");
    }
    else if(fizz === 0){
        console.log("Fizz");
    }
    else {
        console.log(i);
    }
};